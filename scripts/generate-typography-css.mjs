import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const input = resolve(root, 'src/render/typography.css');
const output = resolve(root, 'src/render/typography-generated.ts');

// 1. Run Tailwind CSS CLI to generate the prose CSS.
execFileSync(
  'npx',
  ['tailwindcss', '-i', input, '-o', '/tmp/open-md-tw.css', '--config', resolve(root, 'tailwind.config.cjs')],
  { stdio: 'inherit', cwd: root },
);

// 2. Read the generated CSS.
const css = readFileSync('/tmp/open-md-tw.css', 'utf8').trim();

// 3. Write as a TypeScript string export.
writeFileSync(
  output,
  `// Generated by scripts/generate-typography-css.mjs — DO NOT EDIT.\n// Run "npm run build" to regenerate.\nexport const typographyCss = ${JSON.stringify(css)};\n`,
  'utf8',
);

console.log('Typography CSS generated (%d bytes).', css.length);
